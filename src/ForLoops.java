public class ForLoops {
    public static void main(String[] args) {
        //for loops
        int a = 5;
        for(int i=a; i<=10; i++){
            System.out.println(i);
        }
        //decrement for loops
        int n = 10;
        for(int i=n; i>=0; i--){
            System.out.println(i);
        }
    }
}
