public class Loops {
    public static void main(String[] args) {
        //while loop
        System.out.println("While Loop");
        int num = 5;
        while(num<=10){
            System.out.println(num);
            num++;
        }

        //do - while loop
        System.out.println("Do-While Loop");
        int i = 10;
        do{
            System.out.println(i);
            i++;
        }while(i<9);

        //natural numbers upto 50
        System.out.println("Natural");
        int natural = 1;
        while(natural<=20){
            System.out.println(natural);
            natural++;
        }
    }
}
